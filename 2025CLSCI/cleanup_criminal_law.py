def main():
    with open(INPUT_FILE, 'r') as f:
        articles = json.load(f)

    print(f"清理前: {len(articles)} 篇")

    # 移除误判
    cleaned = []
    removed = []
    for art in articles:
        title = art.get('title', '')
        if title in FALSE_POSITIVES:
            removed.append(art)
        else:
            cleaned.append(art)

    print(f"移除: {len(removed)} 篇")
    for art in removed:
        print(f"  - [{art['journal']}] {art['title'][:50]}")

    print(f"\n清理后: {len(cleaned)} 篇")

    # 按期刊统计
    journal_counts = Counter([a['journal'] for a in cleaned])
    print("\n各期刊刑法文章数:")
    for j, c in sorted(journal_counts.items(), key=lambda x: -x[1]):
        print(f"  {j}: {c}篇")

    # 保存
    with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
        json.dump(cleaned, f, ensure_ascii=False, indent=2)
    print(f"\n保存至: {OUTPUT_FILE}")

    total = 1579  # 有效文章总数
    print(f"\n统计:")
    print(f"  有效文章总数: {total}")
    print(f"  泛刑法文章: {len(cleaned)} ({100*len(cleaned)/total:.1f}%)")


if __name__ == '__main__':
    main()
